/* 
Primitive Data Types - Kiểu dữ liệu nguyên thủy trong JS
1) String : Kiểu chuỗi - kiểu dữ liệu để lưu trữ chuỗi ký tự
2) Number : Kiểu số - Kiểu dữ liệu để lưu trữ số, có thể là số nguyên hoặc số thực
3) Boolean : Kiểu lý luận -  chỉ có thể nhận giá trị true hoặc false
4) Undefined : Biến chưa được gán giá trị
5) Null : Biến được gán giá trị là null, thường được dùng để reset biến hoặc gán giá trị mặc định

*/

// 1) Kiểu String
let myString = `abc`;
console.log(myString);
//Kiểm tra kiểu biến dữ liệu
console.log(typeof myString);

//2) Kiểu Number
let soNguyen = 10;
let soThuc = 3.14;
//xuất giá trị
console.log(soNguyen);
console.log(typeof soNguyen);

console.log(soThuc);
console.log(typeof soThuc);

//3) Kiểu Boolean
let check = true;
console.log(typeof check);
